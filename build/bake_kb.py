"""Bake docs/kb/*.md into license_server/_kb.py as a single string constant.

The license_server package is COPYed into the Fly image, so baking the KB into
it means the support bot has the knowledge base without any extra Docker COPY or
volume. Re-run after editing the KB, then redeploy.

    python build/bake_kb.py
"""
import glob, os

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))   # Aiccounting/
KB_DIR = os.path.join(ROOT, "docs", "kb")
OUT = os.path.join(ROOT, "license_server", "_kb.py")

parts = []
for f in sorted(glob.glob(os.path.join(KB_DIR, "*.md"))):
    if os.path.basename(f) == "index.md":   # meta/navigation, not content
        continue
    parts.append(open(f, encoding="utf-8").read().strip())

kb = "\n\n---\n\n".join(parts)
# guard against the unlikely triple-single-quote collision in the content
safe = kb.replace("'''", "'' '")

with open(OUT, "w", encoding="utf-8") as fh:
    fh.write('"""AUTO-GENERATED by build/bake_kb.py from docs/kb/*.md. Do not edit by hand."""\n')
    fh.write("KB_TEXT = r'''" + safe + "'''\n")

print(f"wrote {OUT}  ({len(kb)} chars from {len(parts)} articles)")
